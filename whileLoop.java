public class whileLoop {
    public static void main(String[] args) {
        
        /*
         döngü kurmadan önce değişkeni tanımla
         while (şartı yaz){
            fonksiyonu yaz.
         }
         */

        int i = 1;
        while(i<10){
            System.out.println(i);
            i++;
        }
        System.out.println("Döngü Bitti");
    }
}
